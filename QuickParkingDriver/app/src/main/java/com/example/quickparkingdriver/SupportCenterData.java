package com.example.quickparkingdriver;

public class SupportCenterData {
    private String supportTitle;
    private String supportDis;
    private String id;
    private String time;


    public String getSupportTitle() {
        return supportTitle;
    }

    public void setSupportTitle(String supportTitle) {
        this.supportTitle = supportTitle;
    }

    public String getSupportDis() {
        return supportDis;
    }

    public void setSupportDis(String supportDis) {
        this.supportDis = supportDis;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
